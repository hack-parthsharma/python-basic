#python methods
#sort ---- it will sort the list in ascendind order
name=[12,25,14,86,2,9,]
name.sort()# it will updated the original list we dont need to store in another fn
print(name)
#reverse-#it will reverse the list NOTE----it will not be in descending order ,it will reverse the list
name.reverse()
print(name)
#append --- add at the end of the list
name.append(34)
print(name)
#insert --- insert at specified index
name.insert(0,1)
print(name)
#pop---it wil remove the item from index
name.pop(0)
print(name)
#remove------------it remove the number of your list
name.remove(14)
print(name)